package academy.learnprograming;

public class Wand {
    private String name;
    private double power;

    public void setPower(double power) {
        this.power = power;
    }
    public void setName(String name) {
        this.name = name;
    }
    public double getPower() {
        return this.power;
    }
    public String getName() {
        return this.name;
    }
}

